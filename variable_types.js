//variable types: String, number, boolean
//typeof it is used for check type of variable
var bananaPrice = 12;
//Number type variable
console.log(typeof bananaPrice); //return: number

//String type variable
var myName = "Alamin";
console.log(typeof myName); //Return: string 

//Boolean type variable.
var isStudent = true;
console.log(isStudent); //Return: true

var isStudent = false;
console.log(isStudent); //Return: false
