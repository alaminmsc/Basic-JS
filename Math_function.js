//Absolute: Math.abs();
var number = -5;
var absoluteNumber = Math.abs(number);
console.log(absoluteNumber); //Return: 5; 
//if have negative number return positive if have positive return positive;


//Round: Math.round();
var gpa = 5.494949;
var result = Math.round(gpa);
console.log(result); //Return: 5


//Ceil: Math.ceil(); return always uper value
var price = 59.499;
var total = Math.ceil(price);
console.log(total); //Return: 60

//Floor: Math.floor(); always return lower value
var bookPrice = 10.5903;
